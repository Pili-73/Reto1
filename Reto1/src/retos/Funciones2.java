package retos;

public class Funciones2 {

	public static boolean esPar (int n) {
		if (n % 2 == 0) 
			return true;
		else
			return false;
	}
	
	public static boolean esImpar (int n) {
		if (n % 2 == 0) 
			return false;
		else
			return true;
	}
	
	public static boolean esPositivo (int n) {
		if (n > 0) 
			return true;
		else
			return false;
	}
	
	public static boolean esNegativo (int n) {
		if (n < 0) 
			return true;
		else
			return false;
	}
	
	
}
